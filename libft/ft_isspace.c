#include "libft.h"

int ft_isspace(char **str)
{
	int i;
	int j;
	
	i = 0;
	while (str && str[i])	
	{
		j = 0;
		while (str[i] && str[i][j])
		{
			if (str[i][j] == ' ')
				return (1);
			j++;
		}
		i++;
	}
	return (0);
}
